package id.meteor.alfamind.Model;


import android.graphics.Bitmap;

public class HighlightModel {

    String id;
    String type;
    String title;
    String description;
    String amount;
    String image;
    String productId;
    private String setImageDetails;

    private Bitmap  bitmap;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public Bitmap getBitmap() {
        return bitmap;
    }

    public void setBitmap(Bitmap bitmap) {
        this.bitmap = bitmap;
    }

    public String getSetImageDetails() {
        return setImageDetails;
    }

    public void setSetImageDetails(String setImageDetails) {
        this.setImageDetails = setImageDetails;
    }
}
